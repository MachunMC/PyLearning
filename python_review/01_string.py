# 字符串常用的方法
# 1. title()：将字符串的所有单词转换成如下格式：首字母大写，其余字母小写
# 2. upper(): 将字符串的所有字母转换成大写形式
# 3. lower()：将字符串的所有字母转换成小写形式
name_li = "LI Ming"
print(name_li)
print(name_li.title())

name_zhang = "zHaNg SAn"
print(name_zhang)
print(name_zhang.title())
print(name_zhang.upper())
print(name_zhang.lower())