# 使用for循环来遍历列表
# for语句后面要有冒号，表示缩进，缩进部分为for循环需要的执行内容
names = ["Tom", "Tony", "Jerry", "Jeny", "Lucy", "Linda"]

print("here are my classmates")

for name in names:
    print(name)

print("that's all")